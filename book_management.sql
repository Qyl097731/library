DROP PROCEDURE IF EXISTS `update`;
DELIMITER ;;
CREATE DEFINER=`root`@`localhost` PROCEDURE `update`()
BEGIN
	#Routine body goes here...
	UPDATE tb_reader SET ifViolate=0;
	UPDATE tb_reader SET ifViolate=1 where username in (
		SELECT username from tb_borrow 
		where bookId in (SELECT id from tb_book where stock=0) and borrowTime!=''
		group by username 
		having  min(TIMESTAMPDIFF(DAY,borrowTime,NOW()))>30
	);
END
;;
